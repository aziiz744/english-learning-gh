// Build lookup map (last entry wins for true duplicates)
const dict: Record<string, string> = {};
for (const [k, v] of rawEntries) {
  dict[k] = v;
}

/**
 * Translate an English word to Arabic.
 * Tries exact match, then strips common suffixes.
 */
export function translate(word: string): string | null {
  const lower = word.toLowerCase().replace(/[^a-z]/g, "");
  if (!lower) return null;

  if (dict[lower]) return dict[lower];

  // Suffix stripping
  const suffixes = ["ing", "tion", "sion", "ness", "ment", "er", "or", "ly", "ed", "es", "s", "ied", "ies"];
  for (const sfx of suffixes) {
    if (lower.endsWith(sfx) && lower.length > sfx.length + 2) {
      const stem = lower.slice(0, lower.length - sfx.length);
      if (dict[stem]) return dict[stem];
      // double-letter stem (running → run)
      if (dict[stem.slice(0, -1)]) return dict[stem.slice(0, -1)];
    }
  }

  return null;
}

/**
 * Returns true if the string is primarily English text.
 */
export function isEnglishText(text: string): boolean {
  if (!text || text.trim().length === 0) return false;
  const letters = text.replace(/[^a-zA-Z\u0600-\u06FF]/g, "");
  if (letters.length === 0) return false;
  const englishChars = (text.match(/[a-zA-Z]/g) || []).length;
  const arabicChars = (text.match(/[\u0600-\u06FF]/g) || []).length;
  return englishChars > arabicChars;
}
